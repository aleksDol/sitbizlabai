function mapSiteType(siteType) {
  if (siteType === "builder") return "Конструктор";
  if (siteType === "custom") return "Самописный";
  return "Неизвестно";
}

export function buildImplementationPlanUserPrompt({
  analysisText,
  lossesText,
  siteType,
  detectedPlatform = null,
  niche = "",
  hasWebsite = true,
  channels = [],
  leadsPerMonth = "",
  hasRepeatSales = ""
}) {
  const channelsLabel = Array.isArray(channels) && channels.length > 0 ? channels.join(", ") : "Не указано";

  return `Контекст для плана:

Ниша: ${niche || "Не указано"}
Есть сайт: ${hasWebsite ? "Да" : "Нет"}
Тип сайта: ${hasWebsite ? mapSiteType(siteType) : "Не применимо (сайта нет)"}
Каналы привлечения: ${channelsLabel}
Заявок в месяц: ${leadsPerMonth || "Не указано"}
Повторные продажи: ${hasRepeatSales || "Не указано"}
detectedPlatform: ${detectedPlatform ? JSON.stringify(detectedPlatform) : "null"}

Анализ:
${analysisText}

Потери:
${lossesText}

Собери план строго по правилам выше и только из релевантных шагов.`;
}
