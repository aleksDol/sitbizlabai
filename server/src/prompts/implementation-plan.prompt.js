export function buildImplementationPlanUserPrompt({
  analysisText,
  lossesText,
  siteType
}) {
  const siteTypeLabel =
    siteType === "builder"
      ? "На конструкторе"
      : siteType === "custom"
        ? "Самописный"
        : "Не знаю";

  return `Данные для персонализированного плана:

Тип сайта: ${siteTypeLabel}

Вот анализ сайта:
${analysisText}

Вот блок о потерях бизнеса:
${lossesText}

Сформируй персонализированный ответ под выбранный тип сайта с практичным следующим шагом реализации.`;
}
